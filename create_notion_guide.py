def api(method, path, data=None):
    url = f"{BASE}{path}"
    body = json.dumps(data).encode() if data else None
    req = urllib.request.Request(url, data=body, method=method)
    req.add_header("Authorization", f"Bearer {MATON_KEY}")
    req.add_header("Notion-Version", "2022-06-28")
    req.add_header("Content-Type", "application/json")
    try:
        resp = urllib.request.urlopen(req)
        return json.loads(resp.read())
    except urllib.error.HTTPError as e:
        print(f"ERROR {e.code}: {e.read().decode()[:300]}")
        return None

def t(content, bold=False, italic=False, color="default"):
    """rich text helper"""
    ann = {}
    if bold: ann["bold"] = True
    if italic: ann["italic"] = True
    if color != "default": ann["color"] = color
    return {"text": {"content": content}, "annotations": ann} if ann else {"text": {"content": content}}

def heading2(text):
    return {"object":"block","type":"heading_2","heading_2":{"rich_text":[t(text)]}}

def heading3(text):
    return {"object":"block","type":"heading_3","heading_3":{"rich_text":[t(text)]}}

def para(text):
    return {"object":"block","type":"paragraph","paragraph":{"rich_text":[t(text)]}}

def bold_para(text):
    return {"object":"block","type":"paragraph","paragraph":{"rich_text":[t(text, bold=True)]}}

def bullet(text):
    return {"object":"block","type":"bulleted_list_item","bulleted_list_item":{"rich_text":[t(text)]}}

def quote(text):
    return {"object":"block","type":"quote","quote":{"rich_text":[t(text)]}}

def callout(emoji, text):
    return {"object":"block","type":"callout","callout":{"icon":{"type":"emoji","emoji":emoji},"rich_text":[t(text)]}}

def divider():
    return {"object":"block","type":"divider","divider":{}}

def numbered(text):
    return {"object":"block","type":"numbered_list_item","numbered_list_item":{"rich_text":[t(text)]}}

def create_subpage(icon, title, children):
    data = {
        "parent": {"type": "page_id", "page_id": PARENT_ID},
        "icon": {"type": "emoji", "emoji": icon},
        "properties": {"title": [{"text": {"content": title}}]},
        "children": children[:100]  # API limit
    }
    result = api("POST", "/pages", data)
    if result and "id" in result:
        page_id = result["id"]
        print(f"✅ {icon} {title} → https://www.notion.so/{page_id.replace('-','')}")
        # If more than 100 blocks, append rest
        if len(children) > 100:
            api("PATCH", f"/blocks/{page_id}/children", {"children": children[100:]})
        time.sleep(0.5)
        return page_id
    else:
        print(f"❌ {title}")
        return None

